// ─── packages/core/src/types/session.ts ───
// 会话管理类型定义 — 会话数据、摘要、存储接口
// 解决问题:
//   1. 定义会话的持久化数据结构，支持会话的保存和恢复（用户关闭 CLI 后再次打开可以继续对话）
//   2. 定义会话摘要结构，用于会话列表页面的高效展示（不加载完整消息历史）
//   3. 定义存储后端接口，支持文件系统、数据库等多种存储实现
//   4. 通过 TokenUsage 跟踪会话的累计 token 消耗，用于成本统计和上下文窗口管理

import type { Message, TokenUsage } from "./messages";

// ─── 会话数据 ───

/**
 * 会话数据 — 持久化到文件系统的完整会话状态
 *
 * 解决问题:
 *   1. 用户关闭终端后对话历史全部丢失是不可接受的 — 需要将消息历史、token 用量、
 *      工作目录等状态持久化，以便下次启动时无缝恢复
 *   2. 会话文件是会话列表（/resume）的数据源
 *   3. 包含 metadata 扩展字段，允许插件或上层应用附加自定义数据
 *
 * 标记: "会话的持久化快照" — 一个 SessionData 对应磁盘上的一个会话文件
 */
export interface SessionData {
    /**
     * 会话唯一 ID — UUID 格式
     *
     * 解决问题: 全局唯一标识一个会话，用于加载、删除和跨引用的关联
     */
    id: string;

    /**
     * 创建时间 — ISO 8601 字符串
     *
     * 解决问题: 会话列表按创建时间排序、展示会话年龄
     */
    createdAt: string;

    /**
     * 最后更新时间 — ISO 8601 字符串
     *
     * 解决问题:
     *   1. 会话列表按活跃度排序（最近使用的排在前面）
     *   2. 自动清理策略可据此删除长期不活跃的会话
     */
    updatedAt: string;

    /**
     * 完整消息历史 — 包含 system/user/assistant 和工具调用/结果
     *
     * 解决问题:
     *   1. 恢复会话后 LLM 需要完整的对话上下文才能继续推理
     *   2. 消息历史是上下文窗口管理和 token 统计的基础数据
     *
     * 标记: "对话的完整记录" — 会话恢复时直接回放给 LLM
     */
    messages: Message[];

    /**
     * 累计 Token 用量 — 本次会话中所有 LLM 调用的 token 消耗总和
     *
     * 解决问题:
     *   1. 成本追踪：让用户了解本次会话花了多少钱
     *   2. 上下文预算：判断当前会话是否接近模型的上下文窗口上限
     *   3. 会话列表中展示 token 消耗概览
     */
    tokenUsage: TokenUsage;

    /**
     * 工作目录 — 会话启动时的工作路径
     *
     * 解决问题: 恢复会话后工具执行应使用原来的工作目录，否则可能出现路径错误
     */
    workingDirectory: string;

    /**
     * 使用的模型标识 — 如 "claude-sonnet-4-6"
     *
     * 解决问题: 恢复会话后应使用相同的模型继续对话，切换模型可能导致行为不一致
     */
    model: string;

    /**
     * 扩展元数据 — 键值对形式的自定义数据
     *
     * 解决问题: 允许插件或上层应用挂载自定义信息（如 Git 分支、项目名称等），
     *          core 层不解析此字段的内容
     */
    metadata: Record<string, unknown>;
}

// ─── 会话摘要 ───

/**
 * 会话摘要 — 会话列表展示用的轻量级结构
 *
 * 解决问题:
 *   1. 会话列表可能包含数百条记录，加载每个会话的完整 messages 数组代价高昂
 *      （内存和 I/O），摘要只包含显示列表所需的最少字段
 *   2. preview 字段提供首条用户消息的前若干字符作为预览，
 *      让用户无需加载完整会话即可识别对话内容
 *
 * 标记: "会话的列表视图" — 仅用于 /resume 等会话选择场景，不可用于恢复对话
 */
export interface SessionSummary {
    /** 会话唯一 ID — 用户选择后用于加载完整 SessionData */
    id: string;

    /** 创建时间 — 用于列表排序和展示 */
    createdAt: string;

    /**
     * 首条用户消息的截断预览 — 通常取前 80-120 个字符
     *
     * 解决问题: 用户在会话列表中通过预览文本快速定位目标会话，
     *          如 "请帮我修复 authentication 模块的 bug..."）
     */
    preview: string;
}

// ─── 会话存储接口 ───

/**
 * 会话存储后端接口 — 会话持久化的抽象契约
 *
 * 解决问题:
 *   1. 默认实现是文件系统存储（JSON 文件），但企业用户可能希望使用数据库
 *   2. 通过接口抽象，只需实现 SessionStore 即可替换存储后端，
 *      无需修改 Agent Loop 或 CLI 的代码
 *   3. 测试场景中可使用内存实现，避免文件 I/O
 *
 * 标记: "会话持久化的可替换后端" — 遵循依赖倒置原则
 */
export interface SessionStore {
    /**
     * 保存会话 — 完整覆盖（有则更新，无则创建）
     *
     * 解决问题: 每轮对话结束后将最新状态持久化，支持崩溃恢复
     *
     * @param session - 包含完整消息历史和元数据的会话对象
     */
    save(session: SessionData): Promise<void>;

    /**
     * 加载会话 — 按 ID 读取完整会话数据
     *
     * 解决问题: 用户选择"继续上次对话"时需要恢复完整的消息上下文
     *
     * @param sessionId - 会话唯一 ID
     * @returns 会话数据，不存在时返回 null（如会话已被删除）
     */
    load(sessionId: string): Promise<SessionData | null>;

    /**
     * 列出所有会话摘要 — 用于会话选择页面
     *
     * 解决问题:
     *   1. 用户执行 /resume 时需要看到所有历史会话的概览
     *   2. 只返回摘要而非完整数据，避免加载数百条消息历史
     *
     * @returns 会话摘要列表，按 updatedAt 降序（最近使用在前）
     */
    list(): Promise<SessionSummary[]>;

    /**
     * 删除会话 — 永久移除会话数据
     *
     * 解决问题: 用户清理不再需要的旧会话以释放磁盘空间
     *
     * @param sessionId - 要删除的会话 ID
     */
    delete(sessionId: string): Promise<void>;
}
